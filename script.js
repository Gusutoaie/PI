function OpenModal() {
    let element = document.getElementById('overlay')
    element.style.display = 'block'
  }
  function estiInregistrat() {
    let element = document.getElementById('esti-inregistrat')
    element.style.display = 'block'
  }
  function inchideInregistrat() {
    let element = document.getElementById('esti-inregistrat')
    element.style.display = 'none'
  }
  function CloseModal() {
    let element = document.getElementById('overlay')
    element.style.display = 'none'
  }
